class Solution {
  public:
    // Function to reverse words in a given string.
    string reverseWords(string str) {
        // code here
        stringstream ss(str);
        string s;
        string ans="";
        while(getline(ss,s,'.')){
            ans=s+ans;
            ans='.'+ans;
        }
        ans.erase(0,1);
        return ans;
    }
};

