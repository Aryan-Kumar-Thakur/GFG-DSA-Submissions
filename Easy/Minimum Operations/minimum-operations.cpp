class Solution
{
  public:
    int minOperation(int n)
    {
        //code here.
        int mini=0;
        while(n){
            if(n&1){
                mini++;
                n--;
            }
            else{
                mini++;
                n=n>>1;
            }
        }
        return mini;
    }
};

